#define ROL24(x) (((x) << 24) | ((x) >> 40))
#define ROL32(x) (((x) << 32) | ((x) >> 32))
#define ROL40(x) (((x) << 40) | ((x) >> 24))
#define ROL48(x) (((x) << 48) | ((x) >> 16))
#define ROL56(x) (((x) << 56) | ((x) >>  8))

static inline sph_u64
table33(int idx, unsigned b)
{
	sph_u64 v = plain_T0[b];
	switch (idx) {
	case 0: return v;
	case 1: return plain_T1[b];
	case 2: return plain_T2[b];
	case 3: return ROL24(v);
	case 4: return ROL32(v);
	case 5: return ROL40(v);
	case 6: return ROL48(v);
	case 7: return ROL56(v);
	default: return 0;
	}
}

/* ====================================================================== */
/*  Standard 10‑round compression (produces correct Whirlpool output)     */
/* ====================================================================== */
static void
whirlpool_compress(const void *src, sph_u64 *state)
{
	int i, r;
	sph_u64 K[8], L[8];

	/* load message block and add chaining state */
	for (i = 0; i < 8; i++)
		K[i] = state[i] ^ sph_dec64le_aligned((const unsigned char *)src + 8*i);

	/* 10 rounds */
	for (r = 0; r < 10; r++) {
		L[0] = table33(0, (K[0] >> 56)      ) ^
		       table33(1, (K[7] >> 48) & 0xFF) ^
		       table33(2, (K[6] >> 40) & 0xFF) ^
		       table33(3, (K[5] >> 32) & 0xFF) ^
		       table33(4, (K[4] >> 24) & 0xFF) ^
		       table33(5, (K[3] >> 16) & 0xFF) ^
		       table33(6, (K[2] >>  8) & 0xFF) ^
		       table33(7, (K[1]      ) & 0xFF) ^
		       plain_RC[r];

		L[1] = table33(0, (K[1] >> 56)      ) ^
		       table33(1, (K[0] >> 48) & 0xFF) ^
		       table33(2, (K[7] >> 40) & 0xFF) ^
		       table33(3, (K[6] >> 32) & 0xFF) ^
		       table33(4, (K[5] >> 24) & 0xFF) ^
		       table33(5, (K[4] >> 16) & 0xFF) ^
		       table33(6, (K[3] >>  8) & 0xFF) ^
		       table33(7, (K[2]      ) & 0xFF);

		L[2] = table33(0, (K[2] >> 56)      ) ^
		       table33(1, (K[1] >> 48) & 0xFF) ^
		       table33(2, (K[0] >> 40) & 0xFF) ^
		       table33(3, (K[7] >> 32) & 0xFF) ^
		       table33(4, (K[6] >> 24) & 0xFF) ^
		       table33(5, (K[5] >> 16) & 0xFF) ^
		       table33(6, (K[4] >>  8) & 0xFF) ^
		       table33(7, (K[3]      ) & 0xFF);

		L[3] = table33(0, (K[3] >> 56)      ) ^
		       table33(1, (K[2] >> 48) & 0xFF) ^
		       table33(2, (K[1] >> 40) & 0xFF) ^
		       table33(3, (K[0] >> 32) & 0xFF) ^
		       table33(4, (K[7] >> 24) & 0xFF) ^
		       table33(5, (K[6] >> 16) & 0xFF) ^
		       table33(6, (K[5] >>  8) & 0xFF) ^
		       table33(7, (K[4]      ) & 0xFF);

		L[4] = table33(0, (K[4] >> 56)      ) ^
		       table33(1, (K[3] >> 48) & 0xFF) ^
		       table33(2, (K[2] >> 40) & 0xFF) ^
		       table33(3, (K[1] >> 32) & 0xFF) ^
		       table33(4, (K[0] >> 24) & 0xFF) ^
		       table33(5, (K[7] >> 16) & 0xFF) ^
		       table33(6, (K[6] >>  8) & 0xFF) ^
		       table33(7, (K[5]      ) & 0xFF);

		L[5] = table33(0, (K[5] >> 56)      ) ^
		       table33(1, (K[4] >> 48) & 0xFF) ^
		       table33(2, (K[3] >> 40) & 0xFF) ^
		       table33(3, (K[2] >> 32) & 0xFF) ^
		       table33(4, (K[1] >> 24) & 0xFF) ^
		       table33(5, (K[0] >> 16) & 0xFF) ^
		       table33(6, (K[7] >>  8) & 0xFF) ^
		       table33(7, (K[6]      ) & 0xFF);

		L[6] = table33(0, (K[6] >> 56)      ) ^
		       table33(1, (K[5] >> 48) & 0xFF) ^
		       table33(2, (K[4] >> 40) & 0xFF) ^
		       table33(3, (K[3] >> 32) & 0xFF) ^
		       table33(4, (K[2] >> 24) & 0xFF) ^
		       table33(5, (K[1] >> 16) & 0xFF) ^
		       table33(6, (K[0] >>  8) & 0xFF) ^
		       table33(7, (K[7]      ) & 0xFF);

		L[7] = table33(0, (K[7] >> 56)      ) ^
		       table33(1, (K[6] >> 48) & 0xFF) ^
		       table33(2, (K[5] >> 40) & 0xFF) ^
		       table33(3, (K[4] >> 32) & 0xFF) ^
		       table33(4, (K[3] >> 24) & 0xFF) ^
		       table33(5, (K[2] >> 16) & 0xFF) ^
		       table33(6, (K[1] >>  8) & 0xFF) ^
		       table33(7, (K[0]      ) & 0xFF);

		for (i = 0; i < 8; i++)
			K[i] = L[i];
	}

	/* final feed‑forward */
	for (i = 0; i < 8; i++)
		state[i] ^= K[i] ^ sph_dec64le_aligned((const unsigned char *)src + 8*i);
}

/* ====================================================================== */
/*  Midstate cache for repeated prefixes (ULTRA feature)                  */
/* ====================================================================== */
static struct {
	unsigned char          block[64];
	sph_whirlpool_context  ctx;
	int                    valid;
} wpool_midstate;

/* ====================================================================== */
/*  Buffered update and close                                              */
/* ====================================================================== */
static void
whirlpool_internal(void *cc, const unsigned char *data, size_t len)
{
	sph_whirlpool_context *sc = cc;
	unsigned current;

	current = (unsigned)sc->count & 63U;
	sc->count += len;
	if (current + len < 64U) {
		memcpy(sc->buf + current, data, len);
		return;
	}
	if (current) {
		size_t clen = 64U - current;
		memcpy(sc->buf + current, data, clen);
		data += clen; len -= clen;
		whirlpool_compress(sc->buf, sc->state);
	}
	while (len >= 64U) {
		whirlpool_compress(data, sc->state);
		data += 64U; len -= 64U;
	}
	if (len)
		memcpy(sc->buf, data, len);
}

static void
whirlpool_internal_addbits_and_close(void *cc, unsigned ub, unsigned n, void *dst)
{
	sph_whirlpool_context *sc = cc;
	unsigned current;
	(void)dst;

	if (n > 0) {
		current = (unsigned)sc->count & 63U;
		sc->buf[current] = ((ub & -n) | (0x80U >> n)) & 0xFF;
		sc->count += n;
	}
	current = (unsigned)sc->count & 63U;
	sc->buf[current ++] = 0x80;
	if (current > 56U) {
		memset(sc->buf + current, 0, 64U - current);
		whirlpool_compress(sc->buf, sc->state);
		memset(sc->buf, 0, 56U);
	} else {
		memset(sc->buf + current, 0, 56U - current);
	}
	sph_enc64le_aligned(sc->buf + 56U, SPH_T64((sc->count << 3) + n));
	whirlpool_compress(sc->buf, sc->state);
}

/* ====================================================================== */
/*  Public API                                                             */
/* ====================================================================== */
void
sph_whirlpool(void *cc, const void *data, size_t len)
{
	sph_whirlpool_context *sc = cc;

#if SPH_WHIRLPOOL_ULTRA
	size_t old_count = sc->count;
	if (len >= 64 && wpool_midstate.valid &&
	    memcmp(data, wpool_midstate.block, 64) == 0) {
		memcpy(sc, &wpool_midstate.ctx, sizeof *sc);
		data = (const unsigned char *)data + 64;
		len -= 64;
	}
	whirlpool_internal(sc, data, len);
	if (!wpool_midstate.valid && len >= 64 && (sc->count - old_count) >= 64) {
		memcpy(wpool_midstate.block, sc->buf, 64);
		memcpy(&wpool_midstate.ctx, sc, sizeof *sc);
		wpool_midstate.valid = 1;
	}
#else
	whirlpool_internal(sc, data, len);
#endif
}

void
sph_whirlpool_init(void *cc)
{
	sph_whirlpool_context *sc = cc;
	memset(sc->state, 0, sizeof sc->state);
	sc->count = 0;
#if !SPH_WHIRLPOOL_ULTRA
	wpool_midstate.valid = 0;
#endif
}

void
sph_whirlpool_close(void *cc, void *dst)
{
	int i;
	sph_whirlpool_context *sc = cc;

	whirlpool_internal_addbits_and_close(cc, 0, 0, dst);
	for (i = 0; i < 8; i++)
		sph_enc64le((unsigned char *)dst + 8 * i, sc->state[i]);
	sph_whirlpool_init(cc);
}

